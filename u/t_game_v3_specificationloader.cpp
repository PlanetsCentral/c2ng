/** Test standard case: load ship list from minimum files.
    A: prepare directory with some files. Load them.
    E: correct data successfully loaded */
void
TestGameV3SpecificationLoader::testStandard()
{
    Environment env;
    env.addStream("beamspec.dat", BEAMSPEC);
    env.addStream("torpspec.dat", TORPSPEC);
    env.addStream("engspec.dat", ENGSPEC);
    env.addStream("hullspec.dat", SHORTENED_HULLSPEC);
    env.addStream("truehull.dat", SHORTENED_TRUEHULL);
    TS_ASSERT_EQUALS(env.load(), true);

    TS_ASSERT_EQUALS(env.list.beams().size(), 10);
    TS_ASSERT_EQUALS(env.list.launchers().size(), 10);
    TS_ASSERT_EQUALS(env.list.engines().size(), 9);
    TS_ASSERT_EQUALS(env.list.hulls().size(), 11);

    TS_ASSERT_EQUALS(env.list.beams().get(1)->getName(env.list.componentNamer()),      "Laser");
    TS_ASSERT_EQUALS(env.list.beams().get(10)->getName(env.list.componentNamer()),     "Heavy Phaser");
    TS_ASSERT_EQUALS(env.list.launchers().get(1)->getName(env.list.componentNamer()),  "Mark 1 Photon");
    TS_ASSERT_EQUALS(env.list.launchers().get(10)->getName(env.list.componentNamer()), "Mark 8 Photon");
    TS_ASSERT_EQUALS(env.list.engines().get(1)->getName(env.list.componentNamer()),    "StarDrive 1");
    TS_ASSERT_EQUALS(env.list.engines().get(9)->getName(env.list.componentNamer()),    "Transwarp Drive");
    TS_ASSERT_EQUALS(env.list.hulls().get(1)->getName(env.list.componentNamer()),      "OUTRIDER CLASS SCOUT");
    TS_ASSERT_EQUALS(env.list.hulls().get(11)->getName(env.list.componentNamer()),     "THOR CLASS FRIGATE");

    TS_ASSERT_EQUALS(env.list.hullAssignments().getHullFromIndex(env.root.hostConfiguration(), 1, 1), 1);
}

