/** Simple functionality test. */
void
TestGameVcrFlakDatabase::testIt()
{
    game::vcr::flak::Database testee;
    TS_ASSERT_EQUALS(testee.getNumBattles(), 0U);
    TS_ASSERT(testee.getBattle(0) == 0);

    afl::io::ConstMemoryStream ms(FILE_CONTENT);
    afl::charset::CodepageCharset cs(afl::charset::g_codepageLatin1);
    afl::string::NullTranslator tx;
    TS_ASSERT_THROWS_NOTHING(testee.load(ms, cs, tx));

    TS_ASSERT_EQUALS(testee.getNumBattles(), 3U);
    TS_ASSERT_EQUALS(testee.getBattle(0)->getNumObjects(), 68U);
    TS_ASSERT_EQUALS(testee.getBattle(2)->getNumObjects(), 8U);
    TS_ASSERT_EQUALS(testee.getBattle(2)->getObject(7, false)->getName(), "Grautvornix");
    TS_ASSERT_EQUALS(testee.getTimestamp().getTimestampAsString(), "09-22-200623:00:51");
}

